
public class Orario {

	private int d;
	private int a;
	private int g;
	private int h;
	
	Orario(int d, int a, int g, int h){
		this.d=d;
		this.a=a;
		this.g=g;
		this.h=h;
	}
	
	public int getD() {
		return d;
	}
	
	public int getA() {
		return a;
	}
	
	public int getG() {
		return g;
	}
	
	public int getH() {
		return h;
	}
	
	
}
